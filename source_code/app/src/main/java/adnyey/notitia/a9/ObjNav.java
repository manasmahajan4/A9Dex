package adnyey.notitia.a9;

public class ObjNav {
    String title;
    int resID;

    public ObjNav(String title, int resID) {
        this.title = title;
        this.resID = resID;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getResID() {
        return resID;
    }

    public void setResID(int resID) {
        this.resID = resID;
    }
}
