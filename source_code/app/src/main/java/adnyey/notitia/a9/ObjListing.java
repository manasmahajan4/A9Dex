package adnyey.notitia.a9;

public class ObjListing {

    int id, stars, classs;
    String brand, name, img;
    Boolean ischecked = false;

    public ObjListing(int id, String brand, String name, int classs, int stars, String img) {
        this.id = id;
        this.stars = stars;
        this.img = img;
        this.brand = brand;
        this.name = name;
        this.classs = classs;
    }

    public Boolean getIschecked() {
        return ischecked;
    }

    public void setIschecked(Boolean ischecked) {
        this.ischecked = ischecked;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStars() {
        return stars;
    }

    public void setStars(int stars) {
        this.stars = stars;
    }

    public int getClasss() {
        return classs;
    }

    public void setClasss(int classs) {
        this.classs = classs;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
