public class ObjImport {
    int quantitiy, cost;

    public ObjImport(int quantitiy, int cost) {
        this.quantitiy = quantitiy;
        this.cost = cost;
    }

    public int getQuantitiy() {
        return quantitiy;
    }

    public int getCost() {
        return cost;
    }
}
