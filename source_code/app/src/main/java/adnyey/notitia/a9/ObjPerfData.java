package adnyey.notitia.a9;

public class ObjPerfData {

    float top, accel, hand, nitro;

    public ObjPerfData(float top, float accel, float hand, float nitro) {
        this.top = top;
        this.accel = accel;
        this.hand = hand;
        this.nitro = nitro;
    }

    public float getTop() {
        return top;
    }

    public float getAccel() {
        return accel;
    }

    public float getHand() {
        return hand;
    }

    public float getNitro() {
        return nitro;
    }
}
